    'InterfaceID',    'IP Valid Tx Data IN', ...
    'InterfaceType',  'IN', ...
    'PortName',       'util_dac_unpack_upack_valid_00', ...
    'PortWidth',      1, ...
    'InterfaceConnection', 'util_ad9361_dac_upack/dac_valid_out_0', ...
    'IsRequired',     false);
	